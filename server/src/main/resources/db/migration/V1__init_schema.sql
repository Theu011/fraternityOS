CREATE TABLE membership_position (
    membership_id BIGINT      NOT NULL REFERENCES membership (id),
    position_id   BIGINT      NOT NULL REFERENCES position (id),
    assigned_at   TIMESTAMPTZ NOT NULL DEFAULT now(),
    PRIMARY KEY (membership_id, position_id)
);

CREATE TABLE announcement (
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    house_id             BIGINT       NOT NULL REFERENCES house (id),
    author_membership_id BIGINT       NOT NULL REFERENCES membership (id),
    title                VARCHAR(200) NOT NULL,
    content              TEXT         NOT NULL,
    is_pinned            BOOLEAN      NOT NULL DEFAULT false,
    created_at           TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at           TIMESTAMPTZ  NOT NULL DEFAULT now()
);
CREATE INDEX idx_announcement_house ON announcement (house_id);

CREATE TABLE event (
    id                       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    house_id                 BIGINT       NOT NULL REFERENCES house (id),
    created_by_membership_id BIGINT       NOT NULL REFERENCES membership (id),
    title                    VARCHAR(200) NOT NULL,
    description              TEXT,
    location                 VARCHAR(200),
    start_date               TIMESTAMPTZ  NOT NULL,
    end_date                 TIMESTAMPTZ,
    created_at               TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at               TIMESTAMPTZ  NOT NULL DEFAULT now()
);
CREATE INDEX idx_event_house ON event (house_id);

CREATE TABLE responsibility (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    house_id        BIGINT       NOT NULL REFERENCES house (id),
    title           VARCHAR(200) NOT NULL,
    description     TEXT,
    type            VARCHAR(20)  NOT NULL,
    rotation_days   INTEGER,
    rotation_cursor INTEGER,
    active          BOOLEAN      NOT NULL DEFAULT true,
    created_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    CONSTRAINT ck_responsibility_type CHECK (type IN ('FIXED', 'ROTATING'))
);
CREATE INDEX idx_responsibility_house ON responsibility (house_id);

CREATE TABLE responsibility_member (
    responsibility_id BIGINT  NOT NULL REFERENCES responsibility (id),
    membership_id     BIGINT  NOT NULL REFERENCES membership (id),
    rotation_order    INTEGER NOT NULL,
    PRIMARY KEY (responsibility_id, membership_id)
);

CREATE TABLE responsibility_assignment (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    responsibility_id BIGINT      NOT NULL REFERENCES responsibility (id),
    membership_id     BIGINT      NOT NULL REFERENCES membership (id),
    status            VARCHAR(20) NOT NULL,
    assigned_date     DATE        NOT NULL,
    due_date          DATE        NOT NULL,
    completed_at      TIMESTAMPTZ,
    CONSTRAINT ck_assignment_status CHECK (status IN ('PENDING', 'COMPLETED', 'OVERDUE'))
);
CREATE INDEX idx_assignment_responsibility ON responsibility_assignment (responsibility_id);
CREATE INDEX idx_assignment_membership     ON responsibility_assignment (membership_id);

CREATE TABLE monthly_statement (
    id                       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    house_id                 BIGINT      NOT NULL REFERENCES house (id),
    uploaded_by_membership_id BIGINT     NOT NULL REFERENCES membership (id),
    month                    INTEGER     NOT NULL,
    year                     INTEGER     NOT NULL,
    notes                    TEXT,
    attachment_url           VARCHAR(500),
    created_at               TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT uq_statement_period UNIQUE (house_id, year, month),
    CONSTRAINT ck_statement_month  CHECK (month BETWEEN 1 AND 12)
);
CREATE INDEX idx_statement_house ON monthly_statement (house_id);

CREATE TABLE payment (
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    monthly_statement_id BIGINT        NOT NULL REFERENCES monthly_statement (id),
    membership_id        BIGINT        NOT NULL REFERENCES membership (id),
    amount               NUMERIC(10,2) NOT NULL,
    status               VARCHAR(20)   NOT NULL,
    paid_at              TIMESTAMPTZ,
    created_at           TIMESTAMPTZ   NOT NULL DEFAULT now(),
    CONSTRAINT uq_payment_membership UNIQUE (monthly_statement_id, membership_id),
    CONSTRAINT ck_payment_status     CHECK (status IN ('PENDING', 'PAID'))
);
CREATE INDEX idx_payment_statement ON payment (monthly_statement_id);
