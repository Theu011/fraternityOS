-- V1: initial FraternityOS schema.
-- Enums are modeled as varchar + CHECK constraints so they map cleanly to
-- JPA @Enumerated(EnumType.STRING) and remain trivial to evolve.
--
-- Identity is split from house membership:
--   * app_user  -- a person's account; exists independently of any house.
--   * house     -- tenant root; every domain row is scoped to a house.
--   * membership -- links one user to one house (a user has at most one).
-- Domain rows reference a MEMBERSHIP (never a user directly), so a person's
-- house-specific history survives even after they go ALUMNI.
-- ("user" is a reserved word in Postgres, hence the app_user table name.)

CREATE TABLE app_user (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name          VARCHAR(150) NOT NULL,
    email         VARCHAR(255) NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    phone         VARCHAR(30),
    created_at    TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at    TIMESTAMPTZ  NOT NULL DEFAULT now(),
    CONSTRAINT uq_app_user_email UNIQUE (email)
);

CREATE TABLE house (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name       VARCHAR(150) NOT NULL,
    created_at TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ  NOT NULL DEFAULT now()
);

CREATE TABLE membership (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id      BIGINT      NOT NULL REFERENCES app_user (id),
    house_id     BIGINT      NOT NULL REFERENCES house (id),
    status       VARCHAR(20) NOT NULL,
    room         VARCHAR(50),
    joined_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
    graduated_at TIMESTAMPTZ,
    created_at   TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at   TIMESTAMPTZ NOT NULL DEFAULT now(),
    -- A user belongs to at most one house.
    CONSTRAINT uq_membership_user   UNIQUE (user_id),
    CONSTRAINT ck_membership_status CHECK (status IN ('ACTIVE', 'ALUMNI'))
);
CREATE INDEX idx_membership_house ON membership (house_id);

-- Positions are global catalog entries (President, Treasurer, Kitchen Manager,
-- ...). They are assigned to memberships and, per the current authorization
-- model, President/Treasurer grant permissions.
CREATE TABLE position (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name        VARCHAR(100) NOT NULL,
    description VARCHAR(500),
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT now(),
    CONSTRAINT uq_position_name UNIQUE (name)
);

